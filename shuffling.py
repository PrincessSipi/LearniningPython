import random
# The random.choice() function returns a randomly selected item from the list
pets = ["Dog", "Cat", "Moose"]
print(random.choice(pets))
print(random.choice(pets))
print(random.choice(pets))


# The random.shuffle() function will reorder items in a list
people = ["Agnes", "Leti", "Bob", "Carol", "David"]
random.shuffle(people)
print(people)
random.shuffle(people)
print(people)
